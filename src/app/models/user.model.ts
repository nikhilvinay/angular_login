export class User {
	public id : Number;
	public first_name : String;
	public last_name : String;
	public email : String;
	public user_name : String;
	public password : String; 

	constructor() {
		this.id = 0;
		this.first_name = '';
		this.last_name = '';
		this.email = '';
		this.user_name = ''
		this.password = ''
	}
	
}
